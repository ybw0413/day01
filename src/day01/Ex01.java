package day01;

public class Ex01 {
	public static void main(String[] args) {
		
		//괄호 안의 출력하는 기능
		/*
		 	여러줄 주석
		*/
		
		System.out.println("안녕하세요!!");
		System.out.println("자바시간입니다");

		System.out.print("출력");
		System.out.print("합니다");
		System.out.print("학원에서 추가");
		System.out.println("집에서 추가");
		
		/*
		 \n : 엔터
		 \t : tab크기만큼 커서를 오른쪽으로 이동
		 \" :"(큰따옴표) 표현
		 \\ : \ 하나 표현
		 */
		System.out.println("안\n녕\n하\n세요");
		System.out.println("1\t123\t12345678\t11");
		System.out.println("  \"  \\  ");
	}
}
